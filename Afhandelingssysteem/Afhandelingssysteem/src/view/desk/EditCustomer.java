    private void UpdateLuggageButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateLuggageButtonActionPerformed
        try {
            fieldsValidator();

            HashMap<String, Object> values = new HashMap<>();

            values.put("FirstName", FirstNameField.getText());
            values.put("LastName", LastNameField.getText());
            values.put("HomeAddress", HomeAddress.getText());
            values.put("StayAddress", StayAddress.getText());
            values.put("Zipcode", Zipcode.getText());
            values.put("City", CityField.getText());
            values.put("Country", CountryField.getText());
            values.put("Phone", PhoneNumberField.getText());
            values.put("Email", Email.getText());

            manager.editCustomer(customer.getId(), values);
            JOptionPane.showMessageDialog(null, "Customer has been edited.");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (SQLException e) {
            Main.exceptionPrint(e);
        }
    }//GEN-LAST:event_UpdateLuggageButtonActionPerformed

