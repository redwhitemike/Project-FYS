    private void submit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit1ActionPerformed
        EmployeeManager employeeManager = new EmployeeManager();

        try {
            fieldsValidator();
            
            int function = -1;

            // Check which function is selected
            if (Admin.isSelected()) {
                function = Main.FUNCTION_APP_MANAGER;
            } else if (Manager.isSelected()) {
                function = Main.FUNCTION_MANAGER;
            } else if (Balie.isSelected()) {
                function = Main.FUNCTION_EMPLOYEE;
            }
            
            // Check if the administrator selected a function
            if (function != -1) {
                // Check if the username exists already
                if (!employeeManager.findUsername(Username.getText())) {
                    if (Password.getText().equals(Repeatpassword.getText())) {
                            HashMap<String, Object> values = new HashMap<>();
                            values.put("Username", Username.getText());
                            values.put("Password", DigestUtils.sha512Hex(Password.getText()));
                            values.put("Email", Email.getText());
                            values.put("FirstName", Name.getText());
                            values.put("Insertion", Insertion.getText());
                            values.put("LastName", Lastname.getText());
                            values.put("Location", Location.getText());
                            values.put("EmployeeNumber", Employeenumber.getText());
                            values.put("Function", function);

                            employeeManager.addEmployee(values);
                            JOptionPane.showMessageDialog(null, "Employee has been added.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Passwords are not the same.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "This username is in use.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "You forgot to select a function.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } catch (HeadlessException e) {
            
        }

    }//GEN-LAST:event_submit1ActionPerformed

